/****************************************************************
  This code is an example to understand the escape sequences used in C
*****************************************************************/

#include <stdio.h>

int main()
{
    
    //////////////// Escape Sequence Usage examples /////////////////////
    printf("Hello World\n");
    printf("David says, \"Programming is fun !\"\n");
    printf("**Conditions apply, \"Offer valid until tomorrow\"\n");
    printf("C:\\My computer\\My folder\n");
    printf("D:/My documents/My file\n");
    printf("\\\\\\\\Today is holiday\\\\\\\\\n");
    printf("\"\"\"This month has 30 days\"\"\"");   
    
    return 0;
}
